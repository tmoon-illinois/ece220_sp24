#include <stdio.h>


int main(){

    int x;

    //while
    x = 0;
    while( x < 10  ){
        printf("%d", x);
        x++;
    }

   //do-while
   x = 0;
   do{
       printf("%d", x);
       x++;
   }while( x < 10);

   //for
   for( x = 0 ; x < 10 ; x++ ){
       printf("%d", x);
   }




}






